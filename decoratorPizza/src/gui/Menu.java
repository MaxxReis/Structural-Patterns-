    public String showPlugins(){
        String names = "";
        ArrayList<String> pluginNames = this.plugins.getLoadedPlugin();
        for (int i = 0; i < pluginNames.size(); i++) {
            names += pluginNames.get(i);
        }
        return names;
    }
    
